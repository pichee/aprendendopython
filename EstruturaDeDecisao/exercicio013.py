#Faça um Programa que leia um número e exiba o dia correspondente da semana. (1-Domingo, 2- Segunda, etc.), se digitar outro valor deve aparecer valor inválido.
dia=int(input("Digite o dia da semana:"))
if dia==1:
  print("Domingo")
if dia==2:
  print("Segunda")
if dia==3:
  print("Terça")
if dia==4:
  print("Quarta")
if dia==5:
  print("Quinta")
if dia==6:
  print("Sexta")
if dia==7:
  print("Sabado")